package ava2019;

public class Logicaloperatorexample {
	public static void main(String[] args) {

		boolean a = false;
        boolean b = true;
        /* logical  operators are same as relational operator except we have to use 
         * boolean data type to solve this problem
         */
		System.out.println(a && b);
		System.out.println(a||b);
		System.out.println(!(a&&b));

	}
}
